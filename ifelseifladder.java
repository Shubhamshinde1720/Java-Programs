class IfElseIfLadder{
public static void main(String args[]){

int temp=99;
 if(temp<=0){
System.out.println("The Water is an ice state :"+ temp);
}else if(temp>=100){
System.out.println("The Water is an vapor state :"+ temp);
}
else{
System.out.println("The Water is an water state :"+ temp);
}
System.out.println("This is if elseif ladder program");

}}