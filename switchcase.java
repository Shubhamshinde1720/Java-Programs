class SwitchCase{
 public static void main(String args[]){
 
int dayofweek = 14;

switch(dayofweek){

case 1:
System.out.println("This is day1 Monday");
break;
case 2:
System.out.println("This is day2 Tuesday");
break;
case 3:
System.out.println("This is day3 Wednesday");
break;
case 4:
System.out.println("This is day4 Thursday");
break;
case 5:
System.out.println("This is day5 Friday");
break;
case 6:
System.out.println("This is day6 Saturday");
break;
case 7:
System.out.println("This is day 7 Sunday");
break;

default:
System.out.println("Not the valid day");
}
}
}