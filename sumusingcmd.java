class SumUsingCmd{
    public static void main(String args[]){
        int sum;

       int num1=Integer.parseInt(args[0]);
       int num2=Integer.parseInt(args[1]);

        sum=num1+num2;
        System.out.println("num1 = "+num1);
        System.out.println("num2 = "+num2);
        
        System.out.println("sum = "+sum);    }
}